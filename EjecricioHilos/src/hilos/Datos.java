package hilos;

public class Datos {

		String nombre, ciudad;
		
		public Datos() {
			nombre="";
			ciudad="";
		}

		public String getNombre() {
			return nombre;
		}

		public void setNombre(String nombre) {
			this.nombre = nombre;
		}

		public String getCiudad() {
			return ciudad;
		}

		public void setCiudad(String ciudad) {
			this.ciudad = ciudad;
		}

		
		


		

		
	

}
